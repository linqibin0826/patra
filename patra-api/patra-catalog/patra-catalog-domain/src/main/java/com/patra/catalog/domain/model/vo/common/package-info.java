/// 通用值对象包。
///
/// 包含跨聚合共享的通用值对象，如去重键等。
///
/// ## 职责
///
/// - **去重键**：封装实体去重的业务标识
///   - **通用概念**：封装可在多个聚合间复用的值对象
///   - **类型安全**：通过强类型避免字符串魔法值
///
/// ## 核心组件
///
/// - {@link com.patra.catalog.domain.model.vo.common.DedupKey} - 去重键值对象
///
/// - 用于标识实体的业务唯一性（如作者的 "姓名 + 机构"）
///       - 支持去重逻辑和冲突检测
///
/// ## 设计原则
///
/// - **业务语义**：去重键基于业务规则，而非技术 ID
///   - **不可变性**：去重键创建后不可修改
///   - **跨聚合共享**：可在不同聚合间使用相同的去重键概念
///
/// ## 使用示例
///
/// ```java
/// // 创建去重键值对象
/// DedupKey dedupKey = DedupKey.of("John Smith | Harvard University");
/// ```
///
/// ## 架构位置
///
/// **Domain 层 - 值对象**：
///
/// - 被多个聚合根使用（跨聚合共享）
/// - 不依赖其他层（纯 Java 对象）
///
/// @author linqibin
/// @since 0.1.0
package com.patra.catalog.domain.model.vo.common;
