-- 文献出版类型管理：
--
-- **业务含义**:
--   出版类型用于分类文献，常见类型包括：
--   - Journal Article（期刊文章）
--   - Review（综述）
--   - Meta-Analysis（荟萃分析）
--   - Clinical Trial（临床试验）
--   - Case Report（病例报告）
--   - Letter（通讯）
--   - Editorial（社论）
--
-- **设计特点**:
--   - 继承 ValueObjectJpaEntity，采用 DELETE/INSERT 模式管理
--   - 存储文献的出版类型信息（可能来自不同受控词表）
--   - 一篇文献可能有多个类型标签
--   - 支持多词表来源（MeSH、Crossref 等）
-- ============================================================


-- ============================================================
-- 表: cat_publication_type (文献出版类型关联表)
-- ============================================================
-- 表说明: 存储文献的出版类型信息，支持多词表来源
-- 记录数预估: 初始 400万 / 年增长 400万 / 5年规模 2400万
-- 主要查询场景:
--   1. 按 publication_id 查询某文献的所有类型(>1000次/天,高频)
--   2. 按 type_value 检索特定类型的文献(>500次/天,中频)
--   3. 按 vocabulary_source 筛选词表来源(<100次/天,低频)
-- ============================================================


CREATE TABLE IF NOT EXISTS `cat_publication_type` (
    -- ========================================
    -- 主键
    -- ========================================
    `id` BIGINT NOT NULL COMMENT '主键,雪花算法预分配',

    -- ========================================
    -- 关联关系
    -- ========================================
    `publication_id` BIGINT NOT NULL COMMENT '出版物ID(外键:cat_publication.id)',

    -- ========================================
    -- 类型信息
    -- ========================================
    `type_id` VARCHAR(50) NULL COMMENT '发表类型的唯一标识符(来自受控词表,可为空)',
    `type_value` VARCHAR(200) NOT NULL COMMENT '发表类型的文本描述(如"Journal Article","Review")',
    `vocabulary_source` VARCHAR(50) NULL COMMENT '词表来源(如"MeSH","Crossref")',
    `type_order` INT NULL COMMENT '类型顺序',

    -- ========================================
    -- 主键和索引
    -- ========================================
    PRIMARY KEY (`id`) COMMENT '主键聚簇索引',

    -- 唯一索引
    UNIQUE INDEX `uk_pub_type` (`publication_id`, `type_value`, `vocabulary_source`) COMMENT '文献+类型值+词表来源唯一索引,避免重复',

    -- 普通索引
    INDEX `idx_publication` (`publication_id`) COMMENT '文献ID索引,支持查询某文献的所有类型(高频)',
    INDEX `idx_type_value` (`type_value`) COMMENT '类型值索引,支持按类型检索(中频)'

) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci
COMMENT='文献出版类型关联表:存储文献的出版类型信息,支持多词表来源';
