package com.patra.starter.redisson.lock;

/// 分布式锁类型枚举。
///
/// @author Patra Team
/// @since 1.0.0
public enum LockType {

    /// 可重入锁（默认）。
    ///
    /// 同一线程可以多次获取同一把锁，释放次数需与获取次数匹配。
    ///
    /// 适用场景：大多数业务场景
    REENTRANT,

    /// 公平锁。
    ///
    /// 按照请求锁的顺序获取锁（先到先得）。
    ///
    /// 适用场景：需要严格顺序保证的场景（如排队系统）
    ///
    /// 警告: 性能略低于可重入锁
    FAIR,

    /// 读锁。
    ///
    /// 允许多个读操作并发执行，但写操作需要独占。
    ///
    /// 适用场景：读多写少的场景
    READ,

    /// 写锁。
    ///
    /// 独占锁，不允许其他读/写操作并发执行。
    ///
    /// 适用场景：需要独占访问的场景
    WRITE
}
