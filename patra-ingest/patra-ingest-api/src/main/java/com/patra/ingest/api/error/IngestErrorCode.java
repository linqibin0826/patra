package com.patra.ingest.api.error;

import com.patra.common.error.codes.ErrorCodeLike;

/// 采集服务的错误代码目录。
/// 
/// 错误代码格式: `ING-NNNN`,其中 ING 是服务前缀。
/// 
/// 代码范围:
/// 
/// - 0xxx: HTTP 标准错误(使用 `HttpStdErrors.of("ING").*` 工厂方法)
///   - 12xx: 注册中心配置错误
///   - 13xx: 发件箱持久化错误
///   - 14xx: 调度器和作业执行错误
///   - 15xx: 检查点序列化错误
///   - 16xx: 计划组装错误
/// 
/// @see com.patra.common.error.codes.ErrorCodeLike
public enum IngestErrorCode implements ErrorCodeLike {

  // ===== 注册中心配置错误 (12xx) =====

  /// 表示注册中心中缺少来源配置。
/// 
/// 在请求注册中心服务中不存在的来源代码配置时发生。请验证来源代码是否已在 patra-registry 中正确注册。
  ING_1201("ING-1201", 404),

  /// 表示注册中心返回了格式错误或无效的配置数据。
/// 
/// 在注册中心响应验证失败或包含不一致数据时发生。请检查注册中心数据完整性和模式合规性。
  ING_1202("ING-1202", 422),

  /// 表示注册中心服务不可达或降级。
/// 
/// 在注册中心服务宕机或遇到问题时发生。系统可能会回退到缓存或默认配置。请监控注册中心服务健康状况。
  ING_1203("ING-1203", 503),

  // ===== 发件箱持久化错误 (13xx) =====

  /// 表示领域事件持久化到发件箱表失败。
/// 
/// 在事务提交时发件箱消息插入失败时发生。表明数据库连接或约束违反问题。请检查数据库日志和连接池。
  ING_1301("ING-1301", 500),

  /// 表示更新发件箱消息状态失败。
/// 
/// 在尝试将发件箱消息标记为已发布或失败时发生。可能表明乐观锁冲突或数据库问题。重试机制应处理瞬态故障。
  ING_1302("ING-1302", 500),

  /// 表示将发件箱消息移动到死信队列失败。
/// 
/// 在超过最大重试次数但死信持久化失败时发生。需要手动干预以防止消息丢失。请检查数据库和死信表模式。
  ING_1303("ING-1303", 500),

  // ===== 调度器和作业执行错误 (14xx) =====

  /// 表示调度作业参数无效或格式错误。
/// 
/// 在作业参数 JSON 解析或业务验证失败时发生。请验证作业配置和参数模式。常见原因包括缺少必需字段或无效的日期格式。
  ING_1401("ING-1401", 422),

  /// 表示调度作业执行期间发生意外失败。
/// 
/// 在作业执行期间遇到未处理异常时发生。请检查应用日志以查看堆栈跟踪和根本原因。可能表明外部服务故障或数据损坏。
  ING_1402("ING-1402", 500),

  /// 表示计划组装预验证失败。
/// 
/// 在计划创建的输入参数在组装开始前未通过业务规则验证时发生。常见原因包括无效的日期范围、负批次大小或禁用的来源。请验证输入参数符合业务约束。
  ING_1403("ING-1403", 422),

  // ===== 检查点序列化错误 (15xx) =====

  /// 表示从存储反序列化检查点负载失败。
/// 
/// 在加载损坏、不兼容或格式错误的检查点数据时发生。可能表明模式演化问题或数据损坏。请验证检查点格式版本兼容性。
  ING_1501("ING-1501", 422),

  /// 表示序列化检查点负载以存储失败。
/// 
/// 在检查点对象无法转换为 JSON 或二进制格式时发生。可能表明循环引用、不支持的类型或编码问题。请检查检查点数据结构。
  ING_1502("ING-1502", 422),

  // ===== 计划持久化错误 (15xx) =====

  /// 表示批次调度和关联任务持久化到数据库失败。
/// 
/// 在计划实体和任务实体的事务性保存期间发生。常见原因包括约束违反、死锁或连接超时。请检查数据库日志和事务隔离设置。
  ING_1503("ING-1503", 500),

  // ===== 计划组装错误 (16xx) =====

  /// 表示组装批次调度及所需任务和切片失败。
/// 
/// 在计划组装逻辑无法生成有效任务切片时发生。可能表明日期范围计算、批次大小逻辑或数据可用性问题。请验证组装参数和来源配置。
  ING_1601("ING-1601", 500);

  private final String code;
  private final int httpStatus;

  IngestErrorCode(String code, int httpStatus) {
    this.code = code;
    this.httpStatus = httpStatus;
  }

  /// 返回错误代码字符串。
/// 
/// @return 格式为 ING-NNNN 的错误代码
  @Override
  public String code() {
    return code;
  }

  /// 返回与此错误关联的 HTTP 状态码。
/// 
/// @return HTTP 状态码 (400-599)
  @Override
  public int httpStatus() {
    return httpStatus;
  }

  /// 返回错误代码的字符串表示形式。
/// 
/// @return 格式为 ING-NNNN 的错误代码
  @Override
  public String toString() {
    return code;
  }
}
